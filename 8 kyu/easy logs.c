// -----------------------------------------------------------
// Add two logs with base X, with the value of A and B. Example log A + log B where the base is X.
// -----------------------------------------------------------

#include <math.h>

double easy_logs(double x, double a, double b) {
  return log(a) / log(x) + log(b) / log(x);
}

